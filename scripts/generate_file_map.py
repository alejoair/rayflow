def _git_ls_files() -> list[str]:
    out = subprocess.run(
        ["git", "ls-files"], cwd=REPO_ROOT, capture_output=True, text=True, check=True,
    ).stdout
    return [line.strip() for line in out.splitlines() if line.strip()]


def _load_old_map() -> dict:
    try:
        return json.loads(FILE_MAP_PATH.read_text(encoding="utf-8"))
    except Exception:
        return {}


def _compute_edges(rel_path: str, tracked: set[str], alias_configs) -> list[str]:
    abs_path = REPO_ROOT / rel_path
    try:
        if rel_path.endswith(".py"):
            edges = set()
            for item in _python_import_items(abs_path):
                edges |= _resolve_python_import_item(item, rel_path, tracked)
            edges.discard(rel_path)
            return sorted(edges)
        if rel_path.endswith((".ts", ".tsx")):
            edges = set()
            for spec in _ts_capture_import_sources(abs_path):
                resolved = _resolve_ts_specifier(spec, rel_path, tracked, alias_configs)
                if resolved and resolved != rel_path:
                    edges.add(resolved)
            return sorted(edges)
    except Exception as exc:
        sys.stderr.write(f"[file-map-generate] failed to compute edges for {rel_path}: {exc!r}\n")
        return []
    return []


def build_files(tracked: list[str], old_files: dict) -> dict:
    tracked_set = set(tracked)
    alias_configs = _load_ts_alias_configs(tracked_set) if _TS_OK else []

    files: dict = {}
    for rel_path in tracked:
        old_entry = old_files.get(rel_path)
        depends_on = _compute_edges(rel_path, tracked_set, alias_configs)

        if old_entry is not None and "_draft_candidate" not in old_entry:
            files[rel_path] = {
                "description": old_entry.get("description", ""),
                "depends_on": depends_on,
                "dependents": [],
                "system": old_entry.get("system"),
            }
            continue

        system_candidate, confidence = _sibling_system_candidate(rel_path, old_files)
        desc_candidate = _description_candidate(rel_path, old_files)
        files[rel_path] = {
            "description": f"[SIN VERIFICAR] {desc_candidate}",
            "depends_on": depends_on,
            "dependents": [],
            "system": system_candidate or "UNASSIGNED",
            "_draft_candidate": {
                "description": desc_candidate,
                "system": system_candidate,
                "system_confidence": confidence,
            },
        }

    for rel_path, entry in files.items():
        for dep in entry["depends_on"]:
            if dep in files:
                files[dep]["dependents"].append(rel_path)
    for entry in files.values():
        entry["dependents"] = sorted(set(entry["dependents"]))

    return files


def build_systems(files: dict, old_systems: dict) -> dict:
    systems: dict = {}
    by_system: dict[str, list[str]] = {}
    for rel_path, entry in files.items():
        sysname = entry.get("system")
        if not sysname or sysname == "UNASSIGNED":
            continue
        by_system.setdefault(sysname, []).append(rel_path)

    for sysname, rel_paths in by_system.items():
        old_info = old_systems.get(sysname, {})
        depends_on_systems = set()
        dependents_systems = set()
        for rel_path in rel_paths:
            for dep in files[rel_path]["depends_on"]:
                dep_sys = files.get(dep, {}).get("system")
                if dep_sys and dep_sys != sysname and dep_sys != "UNASSIGNED":
                    depends_on_systems.add(dep_sys)
            for dependent in files[rel_path]["dependents"]:
                dep_sys = files.get(dependent, {}).get("system")
                if dep_sys and dep_sys != sysname and dep_sys != "UNASSIGNED":
                    dependents_systems.add(dep_sys)
        systems[sysname] = {
            "description": old_info.get("description", ""),
            "macrosistemas": old_info.get("macrosistemas", []),
            "files": sorted(rel_paths),
            "depends_on_systems": sorted(depends_on_systems),
            "dependents_systems": sorted(dependents_systems),
        }
    return systems


def main() -> int:
    try:
        tracked = [p for p in _git_ls_files() if not p.startswith(EXCLUDED_PREFIXES)]
    except Exception as exc:
        sys.stderr.write(f"[file-map-generate] `git ls-files` failed ({exc!r}) -- not touching the file map.\n")
        return 0

    old_map = _load_old_map()
    old_files = old_map.get("files", {})
    old_systems = old_map.get("systems", {})

    files = build_files(tracked, old_files)
    systems = build_systems(files, old_systems)

    new_map = {
        "$schema_note": SCHEMA_NOTE,
        "root": old_map.get("root", ROOT_DESCRIPTION),
        "file_count": len(files),
        "files": {k: files[k] for k in sorted(files)},
        "systems": {k: systems[k] for k in sorted(systems)},
    }

    new_content = json.dumps(new_map, indent=2, ensure_ascii=False) + "\n"
    old_content = FILE_MAP_PATH.read_text(encoding="utf-8") if FILE_MAP_PATH.exists() else None
    if new_content == old_content:
        return 0

    FILE_MAP_PATH.write_text(new_content, encoding="utf-8")
    subprocess.run(["git", "add", str(FILE_MAP_PATH)], cwd=REPO_ROOT, check=False)

    new_stubs = [p for p, e in files.items() if "_draft_candidate" in e]
    sys.stderr.write(
        f"[file-map-generate] Regenerated and staged {FILE_MAP_PATH.name} "
        f"({len(files)} files, {len(new_stubs)} unverified draft(s)"
        + (f": {', '.join(new_stubs[:10])}{'...' if len(new_stubs) > 10 else ''}" if new_stubs else "")
        + ").\n"
    )
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
